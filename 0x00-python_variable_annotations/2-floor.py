#!/usr/bin/env python3


"""
module containing a function to floor a floating point number passed to it.
"""
import math


def floor(n: float) -> int:
    """
    annotated function floor which takes a float n as argument
    and returns the floor of the float.
    :param n: floating point number
    :return: return floor of the param
    """
    return math.floor(n)
